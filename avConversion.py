# -*- coding: utf-8 -*-

def kmhToKn(kmh):
    '''Umrechnung km/h zu kn'''
    return kmh / 1.852

def knToKmh(kn):
    '''Umrechnung kn zu km/h'''
    return kn * 1.852

def mToFt(m):
    '''Umrechnung m zu ft'''
    return m / 0.3048

def ftToM(ft):
    '''Umrechnung ft zu m'''
    return ft * 0.3048

def kgToLb(kg):
    '''Umrechnung kg zu lb'''
    return kg * 2.20462262185

def lbToKg(lb):
    '''Umrechnung lb zu kg'''
    return lb / 2.20462262185

def mpsToFtps(mps):
    '''Umrechnung m/s zu ft/min'''
    return mps * 0.00508

def ftpsToMps(ftps):
    '''Umrechnung ft/min zu m/s'''
    return ftps / 0.00508

